package Day_2;

public class Concatenation_DataTypes {

	public static void main(String[] args) {
		
		int a=100, b=200;
		
		System.out.println("The value of a is "+a);
		System.out.println("The value of b is "+b);
		
		long l = 987123654321L;		// L - Literal
		System.out.println("The value of Long Data Type is "+l);
		 
		float f = 74.47F;		// F - Literal
		System.out.println("The value of Float Data Type is "+f);
		
		byte person_age = 10;
		System.out.println("The Persons age is "+person_age);
		
		char student_grade = 'A';
		System.out.println("The Grade of a student is "+student_grade);
		
		boolean status = true;
		System.out.println("The status is "+status);
		
		String s = "Hello Mama";
		System.out.println("String is "+s);
	}

}
